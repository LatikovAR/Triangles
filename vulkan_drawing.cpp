void Draw_Triangles::build_vert_and_ind_arrays(
        geometry::Objects_and_Intersections&& objs_and_inters)
{
    const std::vector<geometry::Geometry_Object>& objs = objs_and_inters.objects();
    const std::vector<bool>& inters_flags = objs_and_inters.intersection_flags();

    size_t num_of_triangles = 0;
    for(const geometry::Geometry_Object& elem : objs) {
        if(elem.type() == geometry::TRIANGLE) num_of_triangles++;
    }

    vertices.clear();
    indices.clear();

    vertices.reserve(num_of_triangles * 3);
    indices.reserve(num_of_triangles * 6);

    for(const geometry::Geometry_Object& elem : objs) {
        if(elem.type() != geometry::TRIANGLE) continue;

        glm::vec3 color;
        if(inters_flags[elem.number()] == true) {
            color = {1.0f, 0.0f, 0.0f};
        }
        else {
            color = {0.0f, 0.0f, 1.0f};
        }

        const geometry::Triangle& t = elem.triangle();

        geometry::vec norm = t.pl().normal();
        glm::vec3 normal(norm.x(), norm.y(), norm.z());

        geometry::point p = t.p1();
        glm::vec3 pos(p.x(), p.y(), p.z());
        Vertex vert(pos, color, normal);
        vertices.push_back(vert);

        p = t.p2();
        pos = glm::vec3(p.x(), p.y(), p.z());
        vert = Vertex(pos, color, normal);
        vertices.push_back(vert);

        p = t.p3();
        pos = glm::vec3(p.x(), p.y(), p.z());
        vert = Vertex(pos, color, normal);
        vertices.push_back(vert);

        //for drawing one side
        indices.push_back(static_cast<uint16_t>(vertices.size()) - 3);
        indices.push_back(static_cast<uint16_t>(vertices.size()) - 2);
        indices.push_back(static_cast<uint16_t>(vertices.size()) - 1);
        //for drawing other side
        indices.push_back(static_cast<uint16_t>(vertices.size()) - 3);
        indices.push_back(static_cast<uint16_t>(vertices.size()) - 1);
        indices.push_back(static_cast<uint16_t>(vertices.size()) - 2);
    }
}

