VkFormat Draw_Triangles::findSupportedFormat(
        const std::vector<VkFormat>& candidates,
        VkImageTiling tiling,
        VkFormatFeatureFlags features)
{
    for (VkFormat format : candidates) {
        VkFormatProperties props;
        vkGetPhysicalDeviceFormatProperties(physicalDevice, format, &props);

        if (tiling == VK_IMAGE_TILING_LINEAR && (props.linearTilingFeatures & features) == features) {
            return format;
        } else if (tiling == VK_IMAGE_TILING_OPTIMAL && (props.optimalTilingFeatures & features) == features) {
            return format;
        }
    }

    throw std::runtime_error("failed to find supported format!");
}

VkFormat Draw_Triangles::findDepthFormat() {
    return findSupportedFormat(
        {VK_FORMAT_D32_SFLOAT, VK_FORMAT_D32_SFLOAT_S8_UINT, VK_FORMAT_D24_UNORM_S8_UINT},
        VK_IMAGE_TILING_OPTIMAL,
        VK_FORMAT_FEATURE_DEPTH_STENCIL_ATTACHMENT_BIT
    );
}

bool Draw_Triangles::hasStencilComponent(VkFormat format) {
    return format == VK_FORMAT_D32_SFLOAT_S8_UINT || format == VK_FORMAT_D24_UNORM_S8_UINT;
}

void Draw_Triangles_Manager::run() {
    std::vector<geometry::Geometry_Object>start_objs = rotator_.cur_objects_pos(0);

    geometry::Intersection_Finder inters_finder{std::move(start_objs)};
    geometry::Objects_and_Intersections objs_and_inters{
        inters_finder.compute_intersections()};

    Draw_Triangles executor{std::move(objs_and_inters)};

    auto time_null_point = std::chrono::high_resolution_clock::now();
    auto time_last_rotation = time_null_point;

    while(executor.draw_again() == 0) {

        auto cur_time = std::chrono::high_resolution_clock::now();
        double time_after_rotation =
                std::chrono::duration<double, std::chrono::milliseconds::period>
                (cur_time - time_last_rotation).count();

        if(time_after_rotation > POS_LIFETIME) {
            time_last_rotation = cur_time;

            double time_after_start =
                    std::chrono::duration<double,
                    std::chrono::milliseconds::period>
                    (cur_time - time_null_point).count();

            std::vector<geometry::Geometry_Object>cur_objs =
                    rotator_.cur_objects_pos(time_after_start);

            geometry::Intersection_Finder cur_inters_finder{std::move(cur_objs)};
            geometry::Objects_and_Intersections cur_objs_and_inters{
                cur_inters_finder.compute_intersections()};

            executor.update_triangles(std::move(cur_objs_and_inters));
        }
    }
}

